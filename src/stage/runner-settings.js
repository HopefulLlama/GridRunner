class RunnerSettings {
	constructor(maxRunners, spawnChance, spawnDelay, size, speed, trailLength, initialTrailOpacity) {
		this.maxRunners = maxRunners;
		this.spawnChance = spawnChance;
		this.spawnDelay = spawnDelay;
		this.size = size;
		this.speed = speed;
		this.trailLength = trailLength;
		this.initialTrailOpacity = initialTrailOpacity;
	}
}